# Função para gerar uma consulta SQL INSERT aleatória
def generate_insert_query(id, dias_existentes):
    dias_semana = random.choice(["Seg. à Sex.", "Sáb.", "Dom."])
    if dias_semana in dias_existentes:
        return
    horario = random.choice(["Manhã", "Tarde", "Noite", "Fechado"])
    print(f"INSERT INTO AGENDAMENTOS (id_localizacao, dias_semana, horario) VALUES ({id}, '{dias_semana}', '{horario}');");
    return dias_semana

